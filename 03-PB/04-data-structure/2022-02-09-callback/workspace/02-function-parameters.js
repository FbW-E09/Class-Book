/**
 * function parameters
 */

function square10() {
    return 10 * 10;
}

function square5() {
    return 5 * 5;
}
console.log(square10());
console.log(square5());

function square(number) {
    return number * number;
}

console.log(square(10));
console.log(square(5));
console.log(square(4));
console.log(square(2));
